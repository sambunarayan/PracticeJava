package test.operator;

public class JavaOperatorTest {

	public static void main(String[] args) {
		// Shift Operator
		// left shift
		System.out.println("2 << 3 = " + (2 << 3));
		// right shift
		System.out.println("16 >> 3 = " + (16 >> 3));
		System.out.println("-16 >> 3 = " + (-16 >> 3));
		System.out.println("-16 >>> 3 = " + (-16 >>> 3));
	}

}
